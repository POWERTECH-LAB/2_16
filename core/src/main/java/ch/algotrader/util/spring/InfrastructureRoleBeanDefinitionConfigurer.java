package ch.algotrader.util.spring;

import org.springframework.beans.BeansException;
import org.springframework.beans.factory.config.BeanDefinition;
import org.springframework.beans.factory.config.ConfigurableListableBeanFactory;
import org.springframework.beans.factory.support.AbstractBeanDefinition;
import org.springframework.beans.factory.support.BeanDefinitionRegistry;
import org.springframework.beans.factory.support.BeanDefinitionRegistryPostProcessor;

/**
 * Configures all specified beans as infrastructure beans (ROLE_INFRASTRUCTURE)
 *
 * @author <a href="mailto:aflury@algotrader.ch">Andy Flury</a>
 *
 * @version $Revision$ $Date$
 */
public class InfrastructureRoleBeanDefinitionConfigurer implements BeanDefinitionRegistryPostProcessor {

    private String[] beanNames;

    @Override
    public void postProcessBeanFactory(ConfigurableListableBeanFactory beanFactory) throws BeansException {

    }

    @Override
    public void postProcessBeanDefinitionRegistry(BeanDefinitionRegistry registry) throws BeansException {

        if (this.beanNames == null)
            return;

        for (String name : this.beanNames) {
            if (registry.containsBeanDefinition(name)) {
                AbstractBeanDefinition beanDefinition = (AbstractBeanDefinition) registry.getBeanDefinition(name);
                beanDefinition.setRole(BeanDefinition.ROLE_INFRASTRUCTURE);
            }
        }
    }

    public void setBeanNames(String[] beanNames) {
        this.beanNames = beanNames;
    }

}

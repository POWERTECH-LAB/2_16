package com.algoTrader.sabr;

public class SABRCalibrationParams {

    private double r;
    private double v;
    private double a;

    public double getR() {
        return this.r;
    }

    public void setR(double r) {
        this.r = r;
    }

    public double getV() {
        return this.v;
    }

    public void setV(double v) {
        this.v = v;
    }

    public double getA() {
        return this.a;
    }

    public void setA(double a) {
        this.a = a;
    }
}
